function revealEmail(tag) {
  const box = document.getElementById("emailBox" + tag);
  if (!box) return;

  // Toggle
  if (box.style.display === "none" || box.style.display === "") {
    box.style.display = "block";
  } else {
    box.style.display = "none";
  }
}

